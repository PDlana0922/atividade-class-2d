# Programa que lê uma distância em metros e mostra em outras medidas.

m = float(input("Digite uma distância em metros: "))
print(f"A distância de {m}m corresponde a:")
print(f"{m/1000}Km")
print(f"{m/100}Hm")
print(f"{m/10}Dam")
print(f"{m*10}dm")
print(f"{m*100}cm")
print(f"{m*1000}mm")
